    public void mostrarDatos(){
    modelo.setNumRows(contactos.size());
    for(int i=0; i<contactos.size(); i++){
       modelo.setValueAt(contactos.get(i).getNombre(), i, 0);
       modelo.setValueAt(contactos.get(i).getApellido(), i, 1);
       modelo.setValueAt(contactos.get(i).getNumero(), i, 2);
    }
    tablaDatos.setModel(modelo);
}
    

    
    private void Clear_Table(){
        for (int i = 0; i < tablaDatos.getRowCount(); i++) {
        modelo.removeRow(i);
        i-=1;
        }
}
    
    private void cargarArchivo() throws IOException{
        Clear_Table();
        contactos.clear();
        String fila[];
        leer();
        
        try{
           
            FileReader archivo = new FileReader("AgendaContactos.txt");
            BufferedReader lectura = new BufferedReader(archivo);
            
            String linea = lectura.readLine();
           
            
            while(linea != null){
                 fila = linea.split("-");
                 modelo.addRow(fila);
                 linea = lectura.readLine();
            }
            archivo.close();
            lectura.close();
       }catch(FileNotFoundException e){
           System.out.println(e);
       }
}

    public ArrayList<Contacto> leer() {

        try {

            BufferedReader entrada = new BufferedReader(new FileReader("AgendaContactos.txt"));
            String s, s2 = new String();
            int indice;
            String nombre;
            String apellido;
            String numero;
            while ((s = entrada.readLine()) != null) {
                s2 += s + "\n";
                String[] informacion = s.split("-");
                indice = 0;
                nombre = informacion[0];
                apellido = informacion[1];
                numero = informacion[2];
                Contacto objContacto = new Contacto(indice,nombre,apellido,numero) ;             
                contactos.add(objContacto);
            }
            entrada.close();
        } catch (java.io.IOException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL LEER EL ARCHIVO");
        }
        return contactos;
    }


