    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
          
        if(!cajaNombre.getText().equals("") && !cajaApellido.getText().equals("") && !cajaNumero.getText().equals("")){
            if (isNumeric(cajaNumero.getText())) {
                Contacto c = new Contacto(0,"","","");
            
            indice++;
            c.setIndice(indice);
            
            c.setNombre(cajaNombre.getText());
            c.setApellido(cajaApellido.getText());
            c.setNumero(cajaNumero.getText());
            contactos.add(c);
            cajaNombre.setText("");
            cajaApellido.setText("");
            cajaNumero.setText("");
            mostrarDatos();
            }
            else{
                JOptionPane.showMessageDialog(null,"EL NUMERO INGRESADO NO ES VALIDO, INGRESE NÚMEROS ENTEROS");
        }
        
        }else{
        JOptionPane.showMessageDialog(null,"RELLENE TODOS LOS CAMPOS VACIOS");
        }
        
        
    }//GEN-LAST:event_botonGuardarActionPerformed
public static boolean isNumeric(String s)
    {
        if (s == null || s.equals("")) {
            return false;
        }
 
        for (int i = 0; i < s.length(); i++)
        {
            char c = s.charAt(i);
            if (c < '0' || c > '9') {
                return false;
            }
        }
        return true;
    }
